# Создайте список с названиями шести школьных предметов. 
# Спросите у пользователя, какие из этих предметов ему не нравятся. 
# Удалите выбранные предметы из списка и выведите его повторно.

subject = ['Математика', 'Физика', 'Химия', 'Биология', 'Русс.яз', 'Информатика']
print(subject)
subjectDelete = input('Какие предметы Вам не нравятся? - ')
subjectDeleteList = subjectDelete.split(' ')
for i in subjectDeleteList:
    for j in subject:
        if i == j:
            subject.remove(i)
            break

print(subject)

