# Создайте последовательно чисел Фибоначчи до 100.

def searchFib(n):
    if n == 0 or n == 1:
        return 1
    return searchFib(n-1) + searchFib(n-2)

N = 30 #Рекурсия при N > 30 работает уже долго

for i in range(N):
    print(i ,' - ', searchFib(i))

